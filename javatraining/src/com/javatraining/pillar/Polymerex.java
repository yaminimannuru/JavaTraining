package com.javatraining.pillar;

public class Polymerex {
	//if we have multiple method with same name same access specifer/modifier and same return type but diff in signature/parameter that is called compile time polymersim or method overloading
public void eating() {
	System.out.println("cow is eating");
}

public void eating(int a) {
	System.out.println("cat is eating");
}

public void eating(String s) {
	System.out.println("people are eating");
}

public void eating(int a,int b ) {
	System.out.println("dog is eating");
}
public static void main(String[] args) {
	Polymerex pl = new Polymerex();
	pl.eating();
	pl.eating("java");
	pl.eating(12,15);
	pl.eating(10);
}
}
